const shuffleInPlace = <T,>(arr: T[]) => {
  for (let i = arr.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [
      arr[j]!,
      arr[i]!,
    ];
  }
};

export function pickQuizBatch(
  count = 10,
): QuizQuestion[] {
  const copy = [...quizQuestionsAll];
  shuffleInPlace(copy);
  return copy.slice(
    0,
    Math.min(count, copy.length),
  );
}
